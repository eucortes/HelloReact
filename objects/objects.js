//liter notation
let person = {
  Name: "Edgar",
  Age: 47,
  Gender: "Male",
  Active: true

}

console.log(person);

//literal Notation
let person2 = {};

person2.Name = "Edgar";
person2.Age = 47;
person2.Gender = "Male";
person2.Active = true;

console.log(person2);

//bracket notation
let person3 = {};
person3["Name"] = "Edgar";
person3["Age"] = 47;
person3["Gender"] = "Male";
person3["Active"] = true;

console.log(person3);